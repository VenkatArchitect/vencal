def venkats_add (param1, param2):
    return param1 + param2

def venkats_subtract (param1, param2):
    return param1 - param2

def venkats_multiply (param1, param2):
    return param1 * param2

def venkats_divide (param1, param2):
    if (param2 == 0):
        return ("Invalid denominator")
    else:
        return (param1/param2)

